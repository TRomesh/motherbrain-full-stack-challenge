export const convertToDropDown = <T extends { name: string }>(
  data?: T[] | null
): DropDownType[] => {
  if (!data?.length) return [];
  return data.map(({ name }, i) => ({
    key: i.toString(),
    text: name,
    value: name,
  }));
};

export const convertToBarData = <T extends { name: string }>(
  data?: T[] | null
): BarType[] => {
  if (!data?.length) return [];
  return data.map(({ name }) => ({
    name: name,
    value: name,
  }));
};

